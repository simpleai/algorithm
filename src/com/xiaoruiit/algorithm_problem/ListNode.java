package com.xiaoruiit.algorithm_problem;

/**
 * @author hxr
 * @Classname ListNode
 * @Description 力扣链表结点
 */
 public class ListNode {
     int val;
     ListNode next;
     ListNode() {}
     ListNode(int val) { this.val = val; }
     ListNode(int val, ListNode next) { this.val = val; this.next = next; }

 }

